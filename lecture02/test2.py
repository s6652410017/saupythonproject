#ต่อข้อมูลหลายๆ ประเภทเข้าด้วยกัน
#ใฃ้ ,
score = 500
print('hello...',20,True,568,75,55/2,"Hi...",score)
#ใฃ้ +
print('Hello...'+str(20)+str(True)+str(655.45)+str(10/2)+"Hi..."+str(score))
#ใฃ้ เมธอต Format
print('Hello...{} {} {} Hi...{}'.format(20,True,655.45,10/2,score))
#ใฃ้ f'string
print(f'hello... {20} {True} {655.45} {10/2} Hi...{score}')
#ใฃ้ modular operator %
print('Hello... %d %f Hi...' % (20, 17.5) )